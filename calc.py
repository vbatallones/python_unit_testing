def add(x, y):
    """this is add function"""
    return x + y

def subtract(x, y):
    """this is subtract function"""
    return x - y

def multiply(x, y):
    """this is multiply function"""
    return x * y

def divide(x, y):
    """this is divide function"""
    if y == 0:
        return ValueError
    return x / y

